function isValidNodeID(id: string) {
    return !id.includes(".")
}

function validNodesID(nodes: Node[]) {
    for (const node of nodes) {
        if (!isValidNodeID(node.id)) {
            return false;
        }
        if (node.nodes !== undefined && !validNodesID(node.nodes)) {
            return false
        }
    }
    return true;
}

