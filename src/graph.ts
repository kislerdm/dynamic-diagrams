export type Node = {
    /**
     * Node's name used to refer it.
     */
    id: string;
    /**
     * Node's type.
     */
    type: Type;
    /**
     * Human friendly node's description.
     */
    description?: string;
    /**
     * Application, or service technology.
     */
    technology?: string;
    /**
     * Deployment environment for the application, or service.
     */
    deployment?: string;
    /**
     * Node's children.
     */
    nodes?: Node[];
}

/**
 * Node's type.
 */
export enum Type {
    Organisation = "organisation",
    Department = "department",
    Domain = "domain",
    Team = "team",
    Service = "service",
    Application = "application",
    Database = "database",
    Queue = "queue",
}

/**
 * Connection between two Nodes, i.e. graph's edge.
 */
type Link = {
    /**
     * Edge start's Node id.
     */
    from: string;
    /**
     * Edge end's Node id.
     */
    to: string;
    /**
     * Human friendly description of the link between two Nodes.
     */
    description?: string;
    /**
     * Interface technology and protocol, e.g. HTTP/JSON.
     */
    technology?: string;
}

export type graph = {
    nodes: Node[];
    links?: Link[];
}

